    def calcula_regra_jogando(self):
        for movivel in self.moviveis:
            lin = int(movivel.linha)
            col = int(movivel.coluna)
            lin_intencao = int(movivel.linha_intencao)
            col_intencao = int(movivel.coluna_intencao)
            direcoes = self.get_direcoes(lin, col)
            if len(direcoes) >= 3:
                movivel.esquina(direcoes)
            if isinstance(movivel, Fantasma) and movivel.linha == self.pacman.linha and movivel.coluna == self.pacman.coluna:
                self.pacman.vidas -= 1
                if self.pacman.vidas <= 0:
                    self.estado = 2
                else:
                    self.pacman.reset()

            else:
                if 0 <= col_intencao < 28 and 0 <= lin_intencao < 29 and \
                        self.matriz[lin_intencao][col_intencao] != 2:
                    movivel.aceita_movimento()
                    if isinstance(movivel, PacMan) and self.matriz[lin][col] == 1:
                        self.pontos += 1
                        self.matriz[lin][col] = 0
                        if self.pontos >= 306:
                            self.estado = 3
                else:
                    movivel.recusa_movimento(direcoes)

    def pintar_canto(self, tela):
        x = 30 * self.tamanho
        img_pontos = FONTE.render(f'Score: {self.pontos}', True, AMARELO)
        img_vidas = FONTE.render(f'Vidas: {self.pacman.vidas}', True, AMARELO)
        tela.blit(img_pontos, (x, 50))
        tela.blit(img_vidas, (x, 100))

    def pintar(self, tela):
        if self.estado == 0:
            self.pintar_jogando(tela)
        elif self.estado == 1:
            self.pintar_jogando(tela)
            self.pintar_pausado(tela)
        elif self.estado == 2:
            self.pintar_jogando(tela)
            self.pintar_gameover(tela)
        elif self.estado == 3:
            self.pintar_jogando(tela)
            self.pintar_win(tela)

    def pinta_texto_centro(self, tela, texto, cor):
        texto_img = FONTE_P.render(texto, True, cor)
        texto_x = (WIDTH_TELA - texto_img.get_width()) // 2
        texto_y = (HEIGHT_TELA - texto_img.get_width()) // 2
        tela.blit(texto_img, (texto_x, texto_y))

    def pintar_win(self, tela):
        texto = '*** W I N N E R ***'
        cor = AMARELO
        self.pinta_texto_centro(tela, texto, cor)

    def pintar_gameover(self, tela):
        texto = 'G A M E O V E R'
        cor = VERMELHO
        self.pinta_texto_centro(tela, texto, cor)

    def pintar_pausado(self, tela):
        texto = 'P A U S E'
        cor = AMARELO
        self.pinta_texto_centro(tela, texto, cor)

    def pintar_jogando(self, tela):
        for numero_linha, linha in enumerate(self.matriz):
            self.pintar_linha(tela, numero_linha, linha)
        self.pintar_canto(tela)

    def pintar_linha(self, tela, numero_linha, linha):
        for numero_coluna, coluna in enumerate(linha):
            x = numero_coluna * self.tamanho
            y = numero_linha * self.tamanho
            half = self.tamanho // 2
            cor = PRETO
            if coluna == 2:
                cor = AZUL
            pygame.draw.rect(tela, cor, (x, y, self.tamanho, self.tamanho), 0)
            if coluna == 1:
                pygame.draw.circle(tela, AMARELO, (x + half, y + half), self.tamanho // 10, 0)

    def get_direcoes(self, linha, coluna):
        direcoes = []
        if self.matriz[int(linha - 1)][int(coluna)] != 2:
            direcoes.append(UP)
        if self.matriz[int(linha + 1)][int(coluna)] != 2:
            direcoes.append(DOWN)
        if self.matriz[int(linha)][int(coluna - 1)] != 2:
            direcoes.append(LEFT)
        if self.matriz[int(linha)][int(coluna + 1)] != 2:
            direcoes.append(RIGHT)
        return direcoes

    def processa_evento(self, eventos):
        for e in eventos:
            if e.type == pygame.QUIT:
                exit()
            if e.type == pygame.KEYDOWN:
                if e.key == pygame.K_p:
                    if self.estado == 0:
                        self.estado = 1
                    elif self.estado == 1:
                        self.estado = 0
